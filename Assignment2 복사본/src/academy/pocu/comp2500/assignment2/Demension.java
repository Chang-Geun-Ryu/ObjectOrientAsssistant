package academy.pocu.comp2500.assignment2;

public class Demension {
    private int width;
    private int height;

    public Demension(int width, int height) {
        this.width = width;
        this.height = height;
    }

    public int getHeight() {
        return height;
    }

    public int getWidth() {
        return width;
    }
}
