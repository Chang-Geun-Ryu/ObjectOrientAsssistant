package academy.pocu.comp2500.assignment2;

public class Side {
    private int x;
    private int y;

    public Side(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return this.x;
    }

    public int getY() {
        return this.y;
    }
}

